#	include <unistd.h>

void	rec(int nb)
{
	char c;

	if (nb == 0)
		return ;
	rec(nb / 10);
	c = '0' + nb % 10;
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	char c;

	if (nb < 0)
	{
		write(1, "-", 1);
		rec(-(nb / 10));
		c = '0' - nb % 10;
	}
	else
	{
		rec(nb / 10);
		c = '0' + nb % 10;
	}
	write(1, &c, 1);
}
