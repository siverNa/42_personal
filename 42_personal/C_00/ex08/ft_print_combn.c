#	include <unistd.h>

void	ft_print_combn(int n)
{
	char arr[n + 2];
	
	arr[0] = '0';
	arr[n - 1] = ',';
	arr[n] = ' ';
	while (arr[0] != '8')
	{
		arr[1] = arr[0] + 1;
		while (arr[1] != '9')
		{
			write(1, arr, 4);
			++arr[1];
		}
		write(1, arr, 4);
		if (arr[1] == '9')
		{
			++arr[0];
			arr[1] = '0';
		}
		
	}
	write(1, "89", 2);
}
